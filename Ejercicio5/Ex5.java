package Ejercicio5;

public class Ex5 {
public static void main(String[] args) {
	//Lógicos
	System.out.println("Logicos");
	boolean vivo = true;
	System.out.println(vivo);
	//Caracter
	System.out.println("Caracter");
	char letra = 'a';
	System.out.println(letra);
	//Númericos enteros
	System.out.println("Numericos enteros");
	byte num1 = -128;
	System.out.println(num1);
	short num2 = 30000;
	System.out.println(num2);
	int num3 = -2000000000;
	System.out.println(num3);
	long num4 = 7000000000000000L;
	System.out.println(num4);
		
	//Númericos reales
	System.out.println("Numericos reales");
	float num5 = 340282352f;
	System.out.println(num5);
	double num6 = 1797693134862d;
	System.out.println(num6);
	//Constantes
	System.out.println("Constantes");
	final String Nombre ="Hector Gomez Hurtado";
	System.out.println(Nombre);
}
}
